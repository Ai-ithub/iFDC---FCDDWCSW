DROP TABLE IF EXISTS well_records;
CREATE TABLE well_records (
    record_id               SERIAL PRIMARY KEY,
    api_well_id             BIGINT NOT NULL,
    long_deg                DOUBLE PRECISION NOT NULL,
    lat_deg                 DOUBLE PRECISION NOT NULL,
    datetime                TIMESTAMP NOT NULL,
    days_age_well_days      INTEGER NOT NULL,
    drilling_direction      VARCHAR(50) NOT NULL,
    depth_measured_m        DOUBLE PRECISION NOT NULL,
    depth_bit_m             DOUBLE PRECISION NOT NULL,
    layer_id                INTEGER NOT NULL,
    formation_type          VARCHAR(100) NOT NULL,
    layer_type              VARCHAR(50) NOT NULL,
    clay_mineralogy_type    VARCHAR(100) NOT NULL,
    clay_content_percent_p  NUMERIC(6,4) NOT NULL,
    formation_permeability_md DOUBLE PRECISION NOT NULL,
    porosity_formation_p    NUMERIC(5,2) NOT NULL,
    fractures_presence      BOOLEAN NOT NULL,
    phase_operation         VARCHAR(50) NOT NULL,
    weight_on_bit_kg        DOUBLE PRECISION NOT NULL,
    rop_m_hr                DOUBLE PRECISION NOT NULL,
    rpm_rev_min             DOUBLE PRECISION NOT NULL,
    torque_nm               DOUBLE PRECISION NOT NULL,
    mud_type                VARCHAR(50) NOT NULL,
    mud_weight_in_ppg       NUMERIC(4,2) NOT NULL,
    mud_weight_out_ppg      NUMERIC(6,4) NOT NULL,
    mud_temperature_in_c    DOUBLE PRECISION NOT NULL,
    mud_temperature_out_c   DOUBLE PRECISION NOT NULL,
    in_rate_flow_mud_l_min  DOUBLE PRECISION NOT NULL,
    out_rate_flow_mud_l_min DOUBLE PRECISION NOT NULL,
    viscosity_cp            DOUBLE PRECISION NOT NULL,
    mud_ph                  NUMERIC(3,1) NOT NULL,
    fluid_loss_api_ml_30min DOUBLE PRECISION NOT NULL,
    solid_content_p         NUMERIC(5,3) NOT NULL,
    chloride_content_mg_l   DOUBLE PRECISION NOT NULL,
    volume_pit_bbl          DOUBLE PRECISION NOT NULL,
    pressure_standpipe_psi  DOUBLE PRECISION NOT NULL,
    pressure_annulus_psi    DOUBLE PRECISION NOT NULL,
    pressure_reservoir_psi  DOUBLE PRECISION NOT NULL,
    overbalance_psi         DOUBLE PRECISION NOT NULL,
    reservoir_temperature_c DOUBLE PRECISION NOT NULL,
    completion_type         VARCHAR(50) NOT NULL,
    density_perforation_shots_m INTEGER NOT NULL,
    active_damage           VARCHAR(10) NOT NULL,
    type_damage             VARCHAR(50) NOT NULL,
    damage_severity         NUMERIC(4,2) NOT NULL,
    controllable            VARCHAR(10) NOT NULL
);
